import { Schema, model } from 'mongoose';

const messageSchema = new Schema({
  recipient: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
});

export default model('Message', messageSchema);
